package code.datas;

public class User {

//	로그인한 사용자의 이름, 이메일을 저장
	private String email;
	private String name;
	
	
	
	
	
	
	
	
	
	
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
